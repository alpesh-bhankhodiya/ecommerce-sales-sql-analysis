       COUNT(customerid) AS total_customer     
FROM customers
GROUP BY state
ORDER BY total_customer DESC;


-- customer count by city.
SELECT city,
       COUNT(customerid) AS total_customer     
FROM customers 
GROUP BY city
ORDER BY total_customer DESC;


-- Revenue by State
SELECT
    c.state,
    SUM(o.quantity * p.price) AS total_revenue
FROM orders o
JOIN customers c
    ON o.customerid = c.customerid
JOIN products p
    ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY c.state
ORDER BY total_revenue DESC;


-- revenue generated by Android vs iOS users.
SELECT c.operatingsystem,
       SUM(o.quantity * p.price) AS total_revenue
FROM orders o
JOIN products p
   ON p.productid = o.productid
JOIN customers c
   ON c.customerid = o.customerid
WHERE o.deliverystatus = 'Delivered'   
GROUP BY c.operatingsystem;



-- repeat customers (customers with more than 1 order).
SELECT c.customerid,
       CONCAT_WS(' ', c.firstname, c.lastname) AS fullname,
	   COUNT(o.orderid) AS total_order
FROM orders o
JOIN customers c
  ON c.customerid = o.customerid
GROUP BY c.customerid,c.firstname,c.lastname
HAVING COUNT(o.orderid) > 1
ORDER BY total_order DESC;  
 

