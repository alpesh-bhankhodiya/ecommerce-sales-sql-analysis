-- products whose revenue is above the average product revenue.
WITH product_revenue AS (
                   SELECT p.productid,
                          p.productname,
	                      SUM(p.price * o.quantity) AS total_revenue
                   FROM orders o
                   JOIN products p 
                     ON o.productid = p.productid
				   WHERE o.deliverystatus = 'Delivered' 
                   GROUP BY p.productid,p.productname
)

SELECT * FROM product_revenue 
WHERE total_revenue > (
                      SELECT AVG(total_revenue)
					  FROM product_revenue
);


-- Rank all products based on revenue.
WITH product_revenue AS (
                    SELECT p.productid,
                           p.productname,
	                       SUM(p.price * o.quantity) AS total_revenue
                    FROM orders o
                    JOIN products p 
                      ON o.productid = p.productid
					WHERE o.deliverystatus = 'Delivered'  
                    GROUP BY p.productid,p.productname)
 
SELECT DENSE_RANK() OVER(ORDER BY total_revenue DESC) AS revenue_rank,
       *
FROM product_revenue;


-- top 3 revenue-generating products within each category.
WITH product_revenue_category_vise AS (
                    SELECT 
					       p.productid,
                           p.productname,
						   p.category,
	                       SUM(p.price * o.quantity) AS total_revenue
                    FROM orders o
                    JOIN products p 
                      ON o.productid = p.productid
					WHERE o.deliverystatus = 'Delivered'  
                    GROUP BY p.productid,p.productname,p.category
					)

SELECT * 
FROM (
       SELECT 
	         DENSE_RANK() OVER(
                   PARTITION BY category 
				   ORDER BY total_revenue DESC
	         ) AS revenue_ranking_by_category,
	          productid,
              productname,
              category,
              total_revenue          
       FROM product_revenue_category_vise
) t
WHERE revenue_ranking_by_category <= 3;
   
				 
-- monthly revenue trends.

SELECT EXTRACT('Month' FROM o.orderdate) AS month,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
   ON p.productid = o.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY EXTRACT('Month' FROM o.orderdate)
ORDER BY month;

-- OR 
SELECT DATE_TRUNC('Month',o.orderdate) AS month,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
   ON p.productid = o.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY DATE_TRUNC('Month',o.orderdate)
ORDER BY month;



-- the busiest month based on number of orders.
SELECT TO_CHAR(orderdate,'YYYY-MM') AS month,
       COUNT(orderid) AS total_orders
FROM orders
GROUP BY TO_CHAR(orderdate,'YYYY-MM')
ORDER BY total_orders DESC
LIMIT 1;


-- busiest day of the week.
SELECT TO_CHAR(orderdate,'FMDay') AS weekday_num,
       COUNT(orderid) AS total_orders
FROM orders
GROUP BY TO_CHAR(orderdate,'FMDay')
ORDER BY total_orders DESC
LIMIT 1;

-- OR
SELECT EXTRACT(DOW FROM orderdate) AS weekday_num,
       COUNT(orderid) AS total_orders
FROM orders
GROUP BY EXTRACT(DOW FROM orderdate)
ORDER BY total_orders DESC
LIMIT 1;


-- Find the purchase hour generating the highest revenue.
SELECT EXTRACT(Hour FROM o.orderdate) AS purchase_hour,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
   ON p.productid = o.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY EXTRACT(Hour FROM o.orderdate)
ORDER BY total_revenue DESC
LIMIT 1;



-- average delivery time.
SELECT 
	 AVG(DeliveryDate - OrderDate) AS avg_deleivery_time
FROM orders
WHERE deliverystatus = 'Delivered';


-- cancellation rate.
SELECT 
      ROUND(
	  100 * COUNT(orderid)
	  /
     (SELECT
       COUNT(orderid) total_orders
      FROM orders) 
	  ,2
	  ) AS cancellation_rate
FROM orders
WHERE deliverystatus = 'Cancelled';
	   
	 

