CREATE TABLE Customers (
    CustomerID INT PRIMARY KEY,
    FirstName VARCHAR(50),
    LastName VARCHAR(50),
    Phone VARCHAR(20),
    City VARCHAR(50),
    State VARCHAR(50),
    PhoneBrand VARCHAR(50),
    OperatingSystem VARCHAR(20)
);
SELECT * FROM customers;


CREATE TABLE Products (
    ProductID INT PRIMARY KEY,
    ProductName VARCHAR(100),
    Category VARCHAR(50),
    Price DECIMAL(10,2),
    Rating DECIMAL(2,1),
    NumberOfPepopleRated INT
);
SELECT * FROM products;




CREATE TABLE Orders (
    OrderID INT PRIMARY KEY,
    CustomerID INT,
    ProductID INT,
    Quantity INT,
    OrderDate TIMESTAMP,
    DeliveryDate TIMESTAMP,
    DeliveryStatus VARCHAR(20),
    FOREIGN KEY (CustomerID) REFERENCES Customers(CustomerID),
    FOREIGN KEY (ProductID) REFERENCES Products(ProductID)
);
SELECT * FROM orders;


-- total revenue
SELECT SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered';  


-- total number of orders
SELECT COUNT(DISTINCT orderid) AS total_order	   
FROM orders o
JOIN products p
  ON o.productid = p.productid; 

-- average order value
SELECT SUM(p.price * o.quantity)
               /
	   COUNT(DISTINCT orderid) AS AOV	   
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'; 


-- revenue generated by each category.
SELECT p.category,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY p.category
ORDER BY total_revenue DESC;


-- top 5 products by revenue.
SELECT p.productname,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY p.productname
ORDER BY total_revenue DESC
LIMIT 5;


-- Find bottom 5 products by revenue.
SELECT p.productname,
       SUM(p.price * o.quantity) AS total_revenue
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY p.productname
ORDER BY total_revenue 
LIMIT 5;


-- Calculate the percentage contribution of each category to total revenue.
SELECT p.category,
       SUM(p.price * o.quantity) AS total_revenue,
	   ROUND(
	         SUM(p.price * o.quantity) * 100
	             /
	        ( 
			  SELECT SUM(p1.price * o1.quantity)
              FROM orders o1
              JOIN products p1
                 ON o1.productid = p1.productid
              WHERE o1.deliverystatus = 'Delivered'
			 ),
			  2
			 ) AS percentage_contribution
FROM orders o
JOIN products p
  ON o.productid = p.productid
WHERE o.deliverystatus = 'Delivered'
GROUP BY p.category
ORDER BY total_revenue DESC;


